    public void table(){
         String [] piscine={"IDSERVICE","IDCLIENT","OPTION","NOMBRE D'HEURE","TOTAL"};
       String [] afficher=new String [5];
       DefaultTableModel model=new DefaultTableModel(null, piscine);
       try {
           Statement statement=Databasemanager.ouvrirconnection().createStatement();
           FR=statement.executeQuery("SELECT idservice,idclient,nbreheure,optionpiscine,prixtotal FROM piscine");
           while(FR.next()){
               afficher[0]=FR.getString("idservice");
               afficher[1]=FR.getString("idclient");
               afficher[2]=FR.getString("optionpiscine");
               afficher[3]=FR.getString("nbreheure");
               afficher[4]=FR.getString("prixtotal");
               model.addRow(afficher);
           }
           tablepiscine.setModel(model);
           Databasemanager.fermerConnection();
           
       } catch (Exception e) {
           e.printStackTrace();
       }
    }

