    public void table(){
         String [] bar={"IDSERVICE","IDClient","BOISSON","nombre verre","TOTAL"};
       String [] afficher=new String [5];
       DefaultTableModel model=new DefaultTableModel(null, bar);
       try {
           Statement statement=Databasemanager.ouvrirconnection().createStatement();
           FR=statement.executeQuery("SELECT idcom,idclientbar,boisson,nbredeverre,prixbar FROM bar");
           while(FR.next()){
               afficher[0]=FR.getString("idcom");
               afficher[1]=FR.getString("idclientbar");
               afficher[2]=FR.getString("boisson");
               afficher[3]=FR.getString("nbredeverre");
               afficher[4]=FR.getString("prixbar");
               model.addRow(afficher);
           }
           tablebar.setModel(model);
           Databasemanager.fermerConnection();
           
       } catch (Exception e) {
           e.printStackTrace();
       }
    }

